package com.Mini2D.Engine;

public enum Layout {
/////////////////////////////////////////////////////////////////////////////////////////////
//系统定义
//可修改
/////////////////////////////////////////////////////////////////////////////////////////////
BackGround,
Wall,
Sprite,

/////////////////////////////////////////////////////////////////////////////////////////////
//用户定义









/////////////////////////////////////////////////////////////////////////////////////////////
//系统定义
//不能修改
/////////////////////////////////////////////////////////////////////////////////////////////
Default;
	private int value;
	private static class Counter{
	     private static int nextValue = 0;
	}
	Layout(){
		 this(Counter.nextValue);
	}
	Layout(int value) {
		this.value = value;
		Counter.nextValue = value + 1;
	}
	public int getValue(){
	    return value;
	}
}
